package com.getterSetter;

public class StudentGS {
	private int id;  
	private String name,email,country,gender,mobileNumber,studAddress,StudCources,studSection;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getMobileNumber() {
		return mobileNumber;
	}
	public void setMobileNumber(String mobileNumber) {
		this.mobileNumber = mobileNumber;
	}
	public String getStudAddress() {
		return studAddress;
	}
	public void setStudAddress(String studAddress) {
		this.studAddress = studAddress;
	}
	public String getStudCources() {
		return StudCources;
	}
	public void setStudCources(String studCources) {
		StudCources = studCources;
	}
	public String getStudSection() {
		return studSection;
	}
	public void setStudSection(String studSection) {
		this.studSection = studSection;
	}  
}
